u32	get_banner_offset(DiskHeader *header) {
	u32 hash = tinf_crc32((u8*)header, sizeof(DiskHeader));
	hash += 1;

	for (int i = 0; i < bnr_offsets_count; i++) {
		if (bnr_offsets[i].hash == hash)
			return bnr_offsets[i].offset;
	}

	return 0;
}
